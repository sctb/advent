static char mirror[X*Y];

static int reflection(struct robot *r) {
  int k;
  k = abs(XM - r->p.x);
  if (k == 0) return -1;
  return k + (r->p.y * X);
}

static void simulate1(struct robot *r) {
  int x, y;
  x = (r->p.x + r->v.x) % X;
  y = (r->p.y + r->v.y) % Y;
  r->p.x = x >= 0 ? x : X + x;
  r->p.y = y >= 0 ? y : Y + y;
}

int main(int argc, char *argv[]) {
  size_t r;
  int j, m;
  memset(mirror, 0, sizeof(mirror));
  for (r = 0, m = 0; r < nrobots; r++) {
    simulate1(robots+r);
    j = reflection(robots+r);
    /* skip centerpoint */
    if (j < 0) continue;
    /* bail if there's no way to reach symmetry */
    if (m > (nrobots - r)) break;
    /* otherwise add this index or remove it */
    if (mirror[j]) {
      mirror[j] = 0; m--;
    } else {
      mirror[j] = 1; m++;
    }
  }
  printf("ZANY %d\n", m);
  return 0;
}
